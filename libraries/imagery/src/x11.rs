#[cfg(all(test, feature = "std"))]
mod generate {
    use crate::RGBColor;
    use core::str::FromStr;
    use irox_tools::map::OrderedHashMap;
    use std::io::Read;

    #[test]
    #[ignore]
    pub fn generate() {
        let data = include_str!("../data/x11-rgb.txt");
        let mut colors = OrderedHashMap::<RGBColor, String>::new();
        for line in data.lines() {
            let mut ws = irox_tools::scanner::Scanner::new_whitespace(line.as_bytes());
            let r = ws.read_next().unwrap().as_str().to_string();
            let g = ws.read_next().unwrap().as_str().to_string();
            let b = ws.read_next().unwrap().as_str().to_string();
            let mut name = String::default();
            let _ = ws.take_back().read_to_string(&mut name);
            let name = name.trim().to_lowercase();

            if name.contains(" ") {
                continue;
            }
            let r = u8::from_str(&r).unwrap();
            let g = u8::from_str(&g).unwrap();
            let b = u8::from_str(&b).unwrap();
            let col = RGBColor::new(r, g, b);
            if colors.contains(&col) {
                continue;
            }
            colors.insert(col, name);
        }
        for (c, n) in colors.iter() {
            let RGBColor { red, green, blue } = *c;
            println!("(\"{n}\", RGBColor::new({red}, {green}, {blue}).as_color()),")
        }
    }
}
